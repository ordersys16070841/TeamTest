package pojo;

public class MenuClass {
    private int mclass;
    private String className;

    public MenuClass() {
    }

    public int getMclass() {
        return mclass;
    }

    public void setMclass(int mclass) {
        this.mclass = mclass;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }
}
