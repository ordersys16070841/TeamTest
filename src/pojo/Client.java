package pojo;

public class Client {
    private int cId;
    private String cacot;
    private String cpsd;
    private String cname;

    public Client() {
    }

    public int getcId() {
        return cId;
    }

    public void setcId(int cId) {
        this.cId = cId;
    }

    public String getCacot() {
        return cacot;
    }

    public void setCacot(String cacot) {
        this.cacot = cacot;
    }

    public String getCpsd() {
        return cpsd;
    }

    public void setCpsd(String cpsd) {
        this.cpsd = cpsd;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }
}
