package pojo;

public class Waiter {
    private int wid;
    private String wacot;
    private String wpsd;
    private String wname;
    private int wstatus;

    public Waiter() {
    }

    public int getWid() {
        return wid;
    }

    public void setWid(int wid) {
        this.wid = wid;
    }

    public String getWacot() {
        return wacot;
    }

    public void setWacot(String wacot) {
        this.wacot = wacot;
    }

    public String getWpsd() {
        return wpsd;
    }

    public void setWpsd(String wpsd) {
        this.wpsd = wpsd;
    }

    public String getWname() {
        return wname;
    }

    public void setWname(String wname) {
        this.wname = wname;
    }

    public int getWstatus() {
        return wstatus;
    }

    public void setWstatus(int wstatus) {
        this.wstatus = wstatus;
    }
}
